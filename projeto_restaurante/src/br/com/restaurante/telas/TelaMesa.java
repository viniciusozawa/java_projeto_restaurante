    private void btn_salvaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_salvaActionPerformed
        // TODO add your handling code here:
        if(validaCampos()){
           trataEdicao(false);
           int linhaselecionada = tbl_mesa.getSelectedRow();
           Mesa mesa = listMesa.get(linhaselecionada);
           objMesaDAO.salvar(mesa);
           atulizaTabela(); 
        }
        
        
        
    }//GEN-LAST:event_btn_salvaActionPerformed

