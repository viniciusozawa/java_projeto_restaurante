    private void btn_anteriorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_anteriorActionPerformed
        // TODO add your handling code here:
        int linha = tbl_mesa.getSelectedRow();
        if((linha-1)>=0){
            linha--;
        }
        tbl_mesa.setRowSelectionInterval(0, 0);
        tbl_mesa.scrollRectToVisible(tbl_mesa.getCellRect(linha, 0, true));
    }//GEN-LAST:event_btn_anteriorActionPerformed

    private void btn_proximoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_proximoActionPerformed
        // TODO add your handling code here:
        int linha = tbl_mesa.getSelectedRow();
        if((linha +1) <= (tbl_mesa.getRowCount()-1)){
            linha++;
        }
        tbl_mesa.setRowSelectionInterval(linha, linha);
        tbl_mesa.scrollRectToVisible(tbl_mesa.getCellRect(linha, 0, true));
    }//GEN-LAST:event_btn_proximoActionPerformed

