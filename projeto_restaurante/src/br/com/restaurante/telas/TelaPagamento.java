    private void btn_salvaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_salvaActionPerformed
        // TODO add your handling code here:
        if (validaCampos()) {
            trataEdicao(false);
            int linhaselecionada = tbl_pagamentos.getSelectedRow();
            Pagamento objPagamento = listPagamento.get(linhaselecionada);
            objPagamentoDAO.salvar(objPagamento);
            atulizaTabela();
        }


    }//GEN-LAST:event_btn_salvaActionPerformed

