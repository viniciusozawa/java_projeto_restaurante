    public void atulizaTabela() {
        listPagamento.clear();
        listPagamento.addAll(objPagamentoDAO.getLista());
        int linha = listPagamento.size() - 1;
        if (linha >= 0) {
            tbl_pagamentos.setRowSelectionInterval(linha, linha);
            tbl_pagamentos.scrollRectToVisible(tbl_pagamentos.getCellRect(linha, linha, true));
        }
    }

   
    private void trataEdicao(boolean editando) {
        btn_cancelar.setEnabled(editando);
        btn_salva.setEnabled(editando);
        btn_editar.setEnabled(!editando);

        int linha = listPagamento.size() - 1;
        if (linha < 0) {
            btn_excluir.setEnabled(false);
            btn_editar.setEnabled(false);
            int_valor.setText("");
            input_codigo.setText("");
            
            btn_primeiro.setEnabled(false);
            btn_proximo.setEnabled(false);
            btn_anterior.setEnabled(false);
            btn_ultimo.setEnabled(false);

        } else {
            btn_excluir.setEnabled(!editando);
            btn_editar.setEnabled(!editando);
            btn_primeiro.setEnabled(!editando);
            btn_proximo.setEnabled(!editando);
            btn_anterior.setEnabled(!editando);
            btn_ultimo.setEnabled(!editando);
        }
        btn_novo.setEnabled(!editando);
        btn_fechar.setEnabled(!editando);
        int_valor.setEnabled(editando);
        in_itensPag.setEnabled(editando);
        int_valor.setEnabled(editando);
        in_data.setEnabled(editando);
        in_status.setEnabled(editando);
        tbl_pagamentos.setEnabled(editando);

    }

    public boolean validaCampos() {
        if (!(int_valor.getText().length() > 0)) {
            JOptionPane.showMessageDialog(null, "Informe o nome da cidade");
            int_valor.requestFocus();
            return false;
        }
        if (!(in_itensPag.getSelectedIndex() >= 0)) {
            JOptionPane.showMessageDialog(null, "Selecione uma UF");
            in_itensPag.requestFocus();
            return false;
        }
        return true;

    }

